// ─── ДАННЫЕ ДЛЯ РАЗДЕЛА «КЕЙС С ТУ» ───
// 6 кейсов: данные и структура для игровых сессий

// ── Типы ─────────────────────────────────────────────────────────────────────

export interface ReviewRow {
  date: string;
  rating: 1 | 2 | 3;
  text?: string;
  taste?: true;       // Вкус блюд
  atmosphere?: true;  // Обстановка и чистота
  staff?: true;       // Общение персонала
  speed?: true;       // Скорость обслуживания
  temperature?: true; // Температура блюд
  accuracy?: true;    // Точность сборки
}

export interface PlanSection {
  goal: string;       // заголовок блока
  focus: string;      // "Воздействуем на Точность сборки"
  actions: string[];
}

export interface ComplementRow {
  month: string;
  limit: number;
  used: number;
  remaining: number;
  dobroUsed: number;
}

export interface SimpleAction {
  date: string;
  action: string;
}

interface DocBase {
  /** 0-based индекс вопроса, на котором материал становится доступен.
   *  Если не задан — материал доступен сразу (показывается в «Изучи материалы»). */
  unlockAt?: number;
  /** Подпись, поясняющая появление материала по ходу кейса. */
  unlockNote?: string;
}

export type CaseDoc = DocBase & (
  | { type: 'reviews'; title: string; rows: ReviewRow[] }
  | { type: 'plan'; title: string; header?: string; sections: PlanSection[] }
  | { type: 'plan-empty'; title: string; note: string }
  | { type: 'plan-text'; title: string; items: { attribute: string; text: string }[] }
  | { type: 'plan-simple'; title: string; rows: SimpleAction[] }
  | { type: 'complements'; title: string; rows: ComplementRow[] }
);

export interface CaseQuestion {
  facilitatorText: string;
  hint: string;
}

export interface ChecklistItem {
  id: string;
  text: string;
}

export interface GameCase {
  id: string;
  number: number;
  title: string;
  badge: string;
  badgeColor: string; // tailwind classes
  intro: string;
  studyItems: string[];
  preDocs: CaseDoc[];
  preQuestions: CaseQuestion[];
  midDocs?: CaseDoc[];       // для кейса 4: открываются после первых вопросов
  midQuestions?: CaseQuestion[];
  checklistQuestion: string;
  checklistHint: string;     // подсказка ТУ (верный ответ директора) на странице итогов
  checklist: ChecklistItem[];
}

