//
// Логика игры:
//  1. Гость присылает реплику.
//  2. Сотрудник выбирает один из вариантов ответа.
//  3. Если выбор правильный (isCorrect: true) — переходим к следующему шагу.
//  4. Если неправильный — игра заканчивается поражением.
//  5. Последний шаг — это комплемент Гостю (isComplimentStep: true).
//     Если выбран правильный вариант — победа, Гость доволен.

export interface TrainerChoice {
  text: string;
  isCorrect: boolean;
}

export interface TrainerStep {
  guestMessage: string;
  choices: TrainerChoice[];
  correctFeedback: string; // что показываем после правильного ответа (кратко)
  isComplimentStep?: boolean; // последний шаг — комплемент
}

export interface TrainerScenario {
  id: string;
  title: string;
  description: string;
  steps: TrainerStep[];
  winMessage: string;
  winSubtitle: string;
  loseMessage: string;
  loseSubtitle: string;
}

