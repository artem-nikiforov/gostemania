// ─── ДАННЫЕ ДЛЯ РАЗДЕЛА «ЧАТ-ТРЕНАЖЁР» ───
// Логика игры:
//  1. Гость присылает реплику.
//  2. Сотрудник выбирает один из вариантов ответа.
//  3. Если выбор НЕправильный — игра возвращается к Шагу 1 этого же сценария.
//     В режиме «Самостоятельно» — показывается реакция Гостя (selfError).
//     В режиме «С тренером» — показывается подсказка тренера (trainerHint).
//  4. Если выбор правильный — переходим к следующему шагу.
//  5. Последний правильный ответ = победа.

export interface TrainerChoice {
  text: string;
  isCorrect: boolean;
  selfError?: string;    // реакция Гостя при неправильном ответе (режим Самостоятельно)
  trainerHint?: string;  // подсказка тренера при неправильном ответе (режим С тренером)
}

export interface TrainerStep {
  guestMessage: string;
  choices: TrainerChoice[];
  stepLabel?: string; // напр. "Шаг Д+О"
}

export interface TrainerScenario {
  id: string;
  title: string;
  description: string;
  steps: TrainerStep[];
  winMessage: string;
}

